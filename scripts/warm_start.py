# QAOA RUNNER
# ══════════════════════════════════════════════════════════════════════════════

def run_qaoa(qubo, dist_mat, m1, _N, pos_var_map, config, seed, initial_point=None):
    """
    Run QAOA on a position-indexed QUBO with a given config.

    Returns dict with: energy, params (gamma/beta), cost, valid_frac,
                       n_eval, wall_time, found_optimal
    """
    reps = config["reps"]
    opt_name = config["optimizer"].upper()
    maxiter = config["maxiter"]
    n_restarts = config["restarts"]

    ising_op, offset = qubo.to_ising()
    raw = np.array([abs(c) for _, c in ising_op.to_list()], dtype=float)
    op_scale = float(np.max(raw)) if len(raw) > 0 and np.max(raw) > 0 else 1.0
    ising_n = ising_op / op_scale

    algorithm_globals.random_seed = seed
    np.random.seed(seed)

    backend_sv = AerSimulator(method="statevector", seed_simulator=seed)
    est = BackendEstimator(backend=backend_sv)

    ansatz = QAOAAnsatz(ising_n, reps=reps)
    tqc = transpile(ansatz, backend=backend_sv, optimization_level=3)
    n_params = ansatz.num_parameters

    # Track evaluations
    eval_count = [0]

    def _energy(theta, _tqc=tqc):
        theta = np.asarray(theta, dtype=float).ravel()
        job = est.run([_tqc], [ising_n], parameter_values=[theta])
        eval_count[0] += 1
        return float(job.result().values[0]) * op_scale + offset

    if opt_name == "ADAM":
        opt = ADAM(maxiter=maxiter, amsgrad=False)
    elif opt_name == "COBYLA":
        opt = COBYLA(maxiter=maxiter)
    elif opt_name == "SPSA":
        opt = SPSA(maxiter=maxiter)
    else:
        raise ValueError(f"Unknown optimizer: {opt_name}")

    t0 = time.time()
    best_res = None
    for ri in range(n_restarts):
        if initial_point is not None and ri == 0:
            # First restart uses the warm-start point
            x0 = np.array(initial_point, dtype=float)
            if len(x0) != n_params:
                # Pad or trim if param count differs
                if len(x0) < n_params:
                    x0 = np.concatenate([x0, 2 * np.pi * np.random.rand(n_params - len(x0))])
                else:
                    x0 = x0[:n_params]
        else:
            x0 = 2 * np.pi * np.random.rand(n_params)
        res = opt.minimize(fun=_energy, x0=x0)
        if best_res is None or res.fun < best_res.fun:
            best_res = res
    wall_time = time.time() - t0

    # Sample and decode
    circ = tqc.copy()
    if not circ.cregs:
        circ.measure_all()
    bound = circ.assign_parameters(best_res.x, inplace=False)
    counts = backend_sv.run(bound, shots=50_000).result().get_counts()

    # ── Position-indexed decode ──────────────────────────────────────────
    n_qubits = len(qubo.variables)
    total_shots = sum(counts.values())
    valid_count = 0
    best_cost = float("inf")
    best_tour = None

    for bitstr, cnt in counts.items():
        s = bitstr.replace(" ", "")
        bf = np.array([1 if c == "1" else 0 for c in s], dtype=np.int8)
        if len(bf) < n_qubits:
            bf = np.concatenate([np.zeros(n_qubits - len(bf), dtype=np.int8), bf])
        bf = bf[::-1]

        # Extract assignment matrix p[customer, position]
        p = np.zeros((_N, _N), dtype=int)
        for vi, (ci, ti) in pos_var_map.items():
            if vi < len(bf):
                p[ci, ti] = bf[vi]

        # Valid iff doubly-stochastic (each row & col sums to 1)
        if not (np.all(p.sum(axis=1) == 1) and np.all(p.sum(axis=0) == 1)):
            continue

        # Extract route: for each position, which customer?
        perm = tuple(int(np.argmax(p[:, t])) for t in range(_N))
        # Compute cost using dist_mat indices (customer i → dist_mat row i+1)
        cost = dist_mat[0, perm[0] + 1]
        for a, b in zip(perm, perm[1:]):
            cost += dist_mat[a + 1, b + 1]
        cost += dist_mat[perm[-1] + 1, 0]

        valid_count += cnt
        if cost < best_cost:
            best_cost = cost
            best_tour = list(perm)

    valid_frac = valid_count / total_shots if total_shots > 0 else 0.0

    return {
        "energy": float(best_res.fun),
        "params": best_res.x.tolist(),
        "n_params": n_params,
        "cost": float(best_cost) if best_tour else float("inf"),
        "tour": best_tour,
        "valid_frac": valid_frac,
        "n_evals": eval_count[0],
        "wall_time": wall_time,
    }


