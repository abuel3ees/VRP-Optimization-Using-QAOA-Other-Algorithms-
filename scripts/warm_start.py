# QUBO BUILDER — Position-Indexed (k=1, subtour-free)
# ══════════════════════════════════════════════════════════════════════════════

def build_qubo(dist_mat):
    """
    Build position-indexed TSP QUBO for k=1 (node 0 = depot).

    Variables: p_{i}_{t} = 1 if customer i is at position t in the tour.
    N customers → N² binary variables → N² qubits.
    Subtour-free by construction.
    """
    m1 = dist_mat.shape[0]          # total nodes (depot + customers)
    _N = m1 - 1                     # number of customer nodes
    dist_norm = dist_mat / GLOBAL_MAX_DIST
    penalty = _N * 2.0

    qp = QuadraticProgram()
    for i in range(_N):
        for t in range(_N):
            qp.binary_var(f"p_{i}_{t}")

    # ── Objective: route cost ────────────────────────────────────────────
    _lin = {}
    _quad = {}
    for i in range(_N):
        ci = i + 1   # index into dist_norm (0 = depot)
        # depot → first stop
        _lin[f"p_{i}_0"] = _lin.get(f"p_{i}_0", 0) + float(dist_norm[0, ci])
        # last stop → depot
        _lin[f"p_{i}_{_N-1}"] = _lin.get(f"p_{i}_{_N-1}", 0) + float(dist_norm[ci, 0])

    # consecutive-stop costs (quadratic)
    for t in range(_N - 1):
        for i in range(_N):
            ci = i + 1
            for j in range(_N):
                cj = j + 1
                if i != j:
                    key = (f"p_{i}_{t}", f"p_{j}_{t+1}")
                    _quad[key] = _quad.get(key, 0) + float(dist_norm[ci, cj])

    qp.minimize(linear=_lin, quadratic=_quad)

    # ── Assignment constraints ───────────────────────────────────────────
    for i in range(_N):
        qp.linear_constraint(
            {f"p_{i}_{t}": 1 for t in range(_N)},
            sense="==", rhs=1, name=f"cust_{i}")
    for t in range(_N):
        qp.linear_constraint(
            {f"p_{i}_{t}": 1 for i in range(_N)},
            sense="==", rhs=1, name=f"pos_{t}")

    qubo = QuadraticProgramToQubo(penalty=penalty).convert(qp)

    # Build position variable map: var_index → (customer_idx, position)
    var_names = [v.name for v in qubo.variables]
    pos_var_map = {}
    for vi, vn in enumerate(var_names):
        if vn.startswith("p_"):
            parts = vn.split("_")
            pos_var_map[vi] = (int(parts[1]), int(parts[2]))

    return qubo, dist_mat, m1, _N, pos_var_map


# ══════════════════════════════════════════════════════════════════════════════
# CLASSICAL BRUTE-FORCE OPTIMAL
# ══════════════════════════════════════════════════════════════════════════════

def classical_optimal(dist_mat):
    """Brute-force optimal TSP tour (depot=0, k=1) for up to ~8 nodes."""
    m1 = dist_mat.shape[0]
    customers = list(range(1, m1))
    best_cost = float("inf")
    best_perm = None
    for perm in itertools.permutations(customers):
        cost = dist_mat[0, perm[0]]
        for a, b in zip(perm, perm[1:]):
            cost += dist_mat[a, b]
        cost += dist_mat[perm[-1], 0]
        if cost < best_cost:
            best_cost = cost
            best_perm = perm
    return best_cost, list(best_perm)


