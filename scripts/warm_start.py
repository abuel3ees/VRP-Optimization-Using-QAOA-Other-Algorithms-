"""
Experiment 6: Warm-Start QAOA — Variational Parameter Transferability
======================================================================

Overview
--------
This experiment investigates whether the optimal variational parameters
(gamma, beta) of the Quantum Approximate Optimization Algorithm (QAOA),
learned on one sub-instance of the Vehicle Routing Problem (VRP), can
serve as effective initializations for the optimization of structurally
related sub-instances. Successful parameter transfer would allow the
solver to bypass costly re-optimization on each new sub-problem, reducing
total quantum runtime by an estimated 80–90% with no degradation in
solution quality.

QUBO Formulation
----------------
All instances are encoded using a position-indexed QUBO formulation
consistent with the main solver (k=1 vehicle per sub-problem):

    Variable:  p_{i,t} = 1  iff customer i occupies position t in the route

This encoding is subtour-free by construction, eliminating the need for
post-hoc feasibility filtering applied in arc-based formulations.
The resulting Hamiltonian has n^2 binary variables for an n-customer instance.

Benchmark Instances
-------------------
A total of 19 sub-instances are derived from the RioClaroPostToy_50_0
benchmark (50 customers, Euclidean distances) across three problem sizes:

    4-customer (16 qubits) — 13 instances (A through M):
        A–C : Original sub-routes from the main solver decomposition
        D–F : Compact spatial clusters
        G–H : Geographically spread configurations
        I   : Near-depot cluster
        J   : Far-from-depot cluster
        K–M : Mixed-geometry configurations

    2-customer (4 qubits)  — 3 instances (S1–S3): near, far, and mid-distance pairs
    3-customer (9 qubits)  — 3 instances (T1–T3): compact, spread, and mixed geometries

Transfer Pair Design
--------------------
A total of 36 strategically selected warm-start pairs are evaluated,
designed to probe the following transfer scenarios:

    (i)   Homogeneous same-size transfer: compact→compact, spread→spread
          (expected to benefit from structural similarity)
    (ii)  Heterogeneous same-size transfer: compact→spread, near→far
          (expected to show degraded or no benefit)
    (iii) Cross-category and original-pair transfer
    (iv)  Same-size transfer at 2-customer and 3-customer scales
    (v)   Cross-size transfer: 2→3, 2→4, 3→4, 4→3, 4→2
          (parameters are zero-padded or truncated to match target circuit depth)

Experimental Protocol
---------------------
The experiment is conducted in three sequential phases:

    Phase 1 — Cold-Start Baseline:
        Each of the 19 instances is solved independently using QAOA with
        randomly initialized parameters. Five optimizer configurations are
        tested per instance (5 configs × 19 instances = 95 runs total).
        Results establish the cold-start performance ceiling for each instance.

    Phase 2 — Warm-Start Transfer:
        For each of the 36 strategic pairs, the optimal parameters from the
        source instance (obtained in Phase 1) are used as the initial point
        for the optimizer on the target instance. Five configurations are
        evaluated per pair (5 configs × 36 pairs = 180 runs total).
        Improvement over Phase 1 baselines quantifies the benefit of transfer.

    Phase 3 — Parameter Universality:
        The optimized parameters of each instance are applied directly to
        every other same-size instance without any further optimization
        (zero optimizer iterations). This tests the strongest form of
        parameter transferability: the existence of a universal (gamma*, beta*)
        that generalizes across instances of the same size and problem class.
"""

